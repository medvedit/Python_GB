# Напишите программу, каторая на вход принимает число N и выводит все числа от -N до N.

def Range_Number():
    while True:
        number = input('Введите целое, положительное число: ')
        if number.isdigit():
            number1 = int(number) # приводим к зачению int, тк выше бло значение str.
            print(list(range(-number1, number1 + 1))) # или можно вместо list поставить * -> print(*range(-number, number + 1))
            break
Range_Number()

