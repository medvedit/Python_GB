# Напишите программу, каторая на вход принимает целое число и проверяет, кратно ли оно 5 И 10 ИЛИ 15, НО НЕ 30.
def Multiple():
    multiple_num = int(input('Input your number: '))
    if (multiple_num % 5 == 0 and multiple_num % 10 == 0 or multiple_num % 15 == 0) and not multiple_num % 30 == 0:
        print('yes')
    else:
        print('no')

Multiple()

